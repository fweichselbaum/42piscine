void	ft_initarr(int arr[4][4])
{
	int	row;
	int	col;

	row = 0;
	while (row < 4)
	{
		col = 0;
		while (col < 4)
		{
			arr[row][col] = 0;
			col++;
		}
		row++;
	}
}
